package com.simple;

public class SwitchDemo 
{
	public static void main(String[] args) 
	{
		int no=4;
		switch(no)
		{
		case 1:
			System.out.println("ur number is one..");
			break;
		case 2:
			System.out.println("ur number is two..");
			break;
		case 3:
			System.out.println("ur number is three..");
			break;
		default:
			System.out.println("invalid num..");
		}
	}
}
